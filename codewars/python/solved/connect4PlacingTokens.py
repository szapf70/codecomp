# Connect Four - placing tokens

def connect_four_place(columns):
    board = [['-','-','-','-','-','-','-'],
             ['-','-','-','-','-','-','-'],    
             ['-','-','-','-','-','-','-'],
             ['-','-','-','-','-','-','-'],
             ['-','-','-','-','-','-','-'],
             ['-','-','-','-','-','-','-'],]

    heigths = [5,5,5,5,5,5,5]
    
    ap = 'Y'
    np = 'R'

    for c in columns:
        board[heigths[c]][c] = ap
        heigths[c] -= 1
        ap, np = np, ap

    return board

res = connect_four_place([0,1,2,5,6])
for l in res:
    print(l)


